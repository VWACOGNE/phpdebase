    <h1>Le Wakeboard</h1>
         <p>Le wakeboard (de l'anglais wake, sillage, et board, planche) est un sport nautique qui apparait au début des années 1980 après l'avènement du skiboard (qui est désormais le snowboard) à partir d'une combinaison de techniques de ski nautique, de snowboard et de surf. En anglais, wakeboard désigne uniquement la planche, le sport lui-même se disant wakeboarding. Le pratiquant de wakeboard est relié par un palonnier à un bateau à moteur ou un téléski qui le tracte, glisse sur l'eau en se maintenant sur une planche de type surf ou skate</p>
